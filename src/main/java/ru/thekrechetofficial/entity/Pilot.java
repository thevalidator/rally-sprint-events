package ru.thekrechetofficial.entity;

import java.io.Serializable;
import java.time.LocalDate;
import java.time.format.DateTimeFormatter;
import java.util.HashSet;
import java.util.Set;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.EnumType;
import javax.persistence.Enumerated;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.ManyToMany;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;

/**
 * @author theValidator <the.validator@yandex.ru>
 */
@Entity
public class Pilot implements Serializable {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private long id;
    @Column(name = "first_name")
    private String firstName;
    @Column(name = "last_name")
    private String lastName;
    @Enumerated(EnumType.STRING)
    @Column(columnDefinition = "enum", name = "gender")
    private Gender gender;
    private LocalDate birthday;
    @OneToMany(mappedBy = "pilot", fetch = FetchType.LAZY)
    private Set<Vehicle> vehicles = new HashSet<>();
    @OneToOne//(cascade = CascadeType.ALL)
    @JoinColumn(name = "user_id")
    private User user;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Set<Vehicle> getVehicles() {
        return vehicles;
    }

    public void setVehicles(Set<Vehicle> vehicles) {
        this.vehicles = vehicles;
    }
    
    public void addVehicle(Vehicle vehicle) {
        vehicles.add(vehicle);
    }

    public Gender getGender() {
        return gender;
    }

    public void setGender(Gender gender) {
        this.gender = gender;
    }

    public LocalDate getBirthday() {
        return birthday;
    }

    public void setBirthday(LocalDate birthday) {
        this.birthday = birthday;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public int getAge() {
        LocalDate today = LocalDate.now();
        int age = today.minusYears(getBirthday().getYear()).getYear();

        int monthDiff = today.getMonthValue() - getBirthday().getMonthValue();
        int dayDiff = today.getDayOfMonth() - getBirthday().getDayOfMonth();

        if ((monthDiff < 0) || (monthDiff == 0 && dayDiff < 0)) {
            age--;
        }
        return age;
    }
    
    public String getBirthdayFormatted() {
        return birthday.format(DateTimeFormatter.ofPattern("dd.MM.yyyy"));
    }

}
